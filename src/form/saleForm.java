    private void calculate(){
                
        try {
            String id = "";
        int total = 0;
        String quantity = (String) quantityComboBox.getSelectedItem();
        int quantityI = Integer.parseInt(quantity);
        int price = 0;
        String sql = "call getMedicIDByName('"+String.valueOf(medicComboBox.getSelectedItem())+"')";
        try {
            ResultSet rs = c.createStatement().executeQuery(sql);
            if (rs.next()){
                id = rs.getString("tbMedicStorage.med_id");
                sql = "call getMedicPriceByID('"+id+"')";
                rs = c.createStatement().executeQuery(sql);
                if (rs.next()){
                    price = rs.getInt("tbMedicStorage.med_price");
                    total = price * quantityI;
                    totalField.setText(String.valueOf(total));
                    
                } 
                
            } 
        } catch (SQLException e){
            System.out.println(e);
            JOptionPane.showMessageDialog(rootPane, "ຂໍ້ຜິດພາດໄດ້ເກີດຂຶ້ນ: "+ e, "ຂໍ້ຜິດພາດ", JOptionPane.WARNING_MESSAGE);
        }
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "ການຄຳນວນລາຄາມີຂໍ້ຜິດພາດເນືອ່ງຈາກບໍ່ມີຄ່າທີ່ສາມາດຄຳນວນໄດ້\nບໍ່ຕ້ອງສົນໃຈຄຳເຕືອນນີ້", "ຂໍ້ບົກພ່ອງ", JOptionPane.WARNING_MESSAGE);
        }
        
        
        
    }
    
