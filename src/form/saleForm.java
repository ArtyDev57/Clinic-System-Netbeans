    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            int rowCount = saleTable.getRowCount()-1;
            while (rowCount > -1) {                
                model.removeRow(rowCount--);
            }
            String sql = "call initSale()";
            ResultSet rs = c.createStatement().executeQuery(sql);
            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getInt("tbMedicSale.bill_id"));
                row.add(rs.getString("tbMedicSale.customer_name"));
                row.add(rs.getString("tbMedicStorage.med_name"));
                row.add(rs.getInt("tbMedicSale.quantity"));
                row.add(rs.getInt("tbMedicSale.total_price"));
                row.add(rs.getDate("tbMedicSale.bill_date"));
                model.addRow(row);
            }
            rs.close();
            saleTable.setModel(model);
            
            sql = "call getMedicName()";
            rs = c.createStatement().executeQuery(sql);
            while (rs.next()) {                
                med.addElement(rs.getString("med_name"));
            }
            rs.close();
            medicComboBox.setModel(med);
            
            calculate();
        } catch (SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

    private void saleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saleButtonActionPerformed
        // TODO add your handling code here:
        String customerF = customerField.getText();
        String medicF = (String)medicComboBox.getSelectedItem();
        String medID = "";
        String quantityS = (String)quantityComboBox.getSelectedItem();
        int quantity = Integer.parseInt(quantityS);
        int stockQuantity = 0;
        int statementNumber = 0;
        if (customerF.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "ກະລຸນາໃສ່ຊື່ລູກຄ້າ", "ຂໍ້ຜິດພາດ", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
            String sqlR = "call getMedicIDByName('"+medicF+"')";
            ResultSet rs = c.createStatement().executeQuery(sqlR);
            if (rs.next()){
                medID = rs.getString("tbMedicStorage.med_id");
            }
            sqlR = "call getMedicStockQuantityByID('"+medID+"')";
            rs = c.createStatement().executeQuery(sqlR);
            if (rs.next()){
                stockQuantity = rs.getInt("tbMedicStorage.med_quantity");
            }
                if (stockQuantity >= quantity && stockQuantity != 0){
                String sql = "call addSale('"+customerF+"', '"+medID+"', "+quantity+")";
                Statement s = c.createStatement();
                statementNumber = s.executeUpdate(sql);
                if (statementNumber != 0){
                    JOptionPane.showMessageDialog(rootPane, "ຂາຍແລ້ວ", "ສຳເລັດຜົນ", JOptionPane.INFORMATION_MESSAGE);
                    this.formWindowOpened(null);
                } else {
                JOptionPane.showMessageDialog(rootPane, "ດຳເນີນການບໍ່ສຳເລັດ, ກະລຸນາລອງໃໝ່", "ບໍ່ສຳເລັດຜົນ", HEIGHT);
                }
            } else {
                    JOptionPane.showMessageDialog(rootPane, "ຈຳນວນຢາໃນຄັງບໍ່ພຽງພໍຫຼືບໍ່ມີຢາຢູ່ໃນຄັງ", "ຂໍ້ຜິດພາດ", HEIGHT);
            }
            
            
        } catch(SQLException e){
            System.out.println(e);
            JOptionPane.showMessageDialog(rootPane, "ຂໍ້ຜິດພາດໄດ້ເກີດຂຶ້ນ: "+ e, "ຂໍ້ຜິດພາດ", JOptionPane.WARNING_MESSAGE);
        }
        }
    }//GEN-LAST:event_saleButtonActionPerformed

